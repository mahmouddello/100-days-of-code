# HTTP Requests:

# Get, Post, Put, Delete. => Usage: requests.get(), requests.post(), requests.put(), requests.delete()

# Get: Asking for an external system a piece of data, and we get it as a response

# Post: Sending out a piece of data to the external system. We don't care about the response,
# the important thing is the post process was successful or not

# Put: Update a piece of data in a external system.

# Delete : Delete a piece of data in a external system.
