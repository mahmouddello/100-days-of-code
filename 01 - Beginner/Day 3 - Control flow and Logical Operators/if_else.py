# Selection / Decision Statements :
# simple if
# if - else
# nested if
# nested if - else
#####################################
# if condition:
#    do this
# else:
#    do this
#####################################
water_level = 90
if water_level > 90:
    print("Drain water")
else:
    print("No Problem !")
# So in this example, first we are checking on the first if statement;
# (if) the first condition is true we should drain our water ,
# (else) if the first condition is false that means we are going to execute our second condition
# ! ! Note : if the first condition  execute we don't move to the next condition ever !
