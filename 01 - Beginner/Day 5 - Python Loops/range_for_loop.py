# for loop with range
for i in range(1, 10):
    # between 1 and 10 and NOT including 10
    print(i)
print("----------------------")
for j in range(1, 10, 3):
    # between 1 and 10 and NOt including 10 and moving by 2 steps forward
    print(j)
# Third argument in the range function is about how many steps you want to specify. by default is
print("------------------------")
