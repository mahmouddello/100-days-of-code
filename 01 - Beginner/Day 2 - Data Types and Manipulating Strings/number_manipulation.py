# Example :
print(8 / 3)  # 2.666666666666665
# If you want the result printed like integer number without using the int() function:
# you can use the flow division (//)
print(8 // 3)  # basically normal dividing operation but the second / operator to get red of the floating numbers
# We can use a method to round() a number instead of using int() to get red of the decimal precisions
print(round(8 / 3))
# You can specify the number of digits of precision in the second argument :
print(round(8 / 3, 2))
